public class operadores {
    public static void main(String[] args)
    {
        int k = 10; 

        int i = ++k;
        int j = k--; 
        int a = k; 
    
        System.out.println(k);  
        System.out.println(i); 
        System.out.println(j); 
        System.out.println(a); 
        System.out.println(k + i); 
    }
}
